def gen_product(name, features_str):
    """产品介绍话术"""
    features = [f.strip() for f in features_str.split(",")]

    print("=" * 60)
    print("📦 产品介绍话术 | 产品: {} | 卖点: {}".format(name, ", ".join(features)))
    print("=" * 60)
    print("")

    print(textwrap.dedent("""\
        ## 🎯 产品介绍框架（FABE法则）

        ### Feature（特征）
        来，家人们看这个{name}。
        先看包装——[描述包装/外观]
        打开看——[描述产品细节]

        这个{name}最大的特点是什么？就是{feature_main}！
    """.format(name=name, feature_main=features[0] if features else "品质好")))

    print("### Advantage（优势）")
    print("跟市面上其他的{}比，这个有什么不同？".format(name))
    print("")
    for i, feature in enumerate(features, 1):
        print("**卖点{}：{}**".format(i, feature))
        print("你们看——[展示/演示]")
        print("[用对比、数据或亲身体验来证明]")
        print("")

    print(textwrap.dedent("""\
        ### Benefit（好处）
        用了这个{name}之后，你会发现——
        [描述使用后的效果/感受]
        [用场景化的语言，让观众想象自己使用的画面]

        ### Evidence（证据）
        这不是我一个人说好！
        - 你们看这个评价——[展示好评截图]
        - 已经卖了XX万件了
        - [如有] XX明星/达人也在用

        ---

        ## 🔥 产品演示话术

        来，我现在给你们演示一下。

        [实际操作演示]

        你们看到了吗？{feature_main}！这个效果是不是很明显？

        有没有想要的？想要的扣1！

        [读弹幕互动]

        好！我看到很多宝子扣1了，别着急，价格马上揭晓！
    """.format(name=name, feature_main=features[0] if features else "效果")))


