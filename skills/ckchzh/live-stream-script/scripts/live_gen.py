def main():
    if len(sys.argv) < 2:
        print("用法: live_gen.py <command> [args]")
        sys.exit(1)

    cmd = sys.argv[1]

    if cmd == "open":
        if len(sys.argv) < 3:
            print("用法: live_gen.py open \"主题\"")
            sys.exit(1)
        gen_open(sys.argv[2])

    elif cmd == "product":
        if len(sys.argv) < 4:
            print("用法: live_gen.py product \"产品名\" \"卖点1,卖点2\"")
            sys.exit(1)
        gen_product(sys.argv[2], sys.argv[3])

    elif cmd == "close":
        if len(sys.argv) < 4:
            print("用法: live_gen.py close \"产品名\" \"价格\"")
            sys.exit(1)
        gen_close(sys.argv[2], sys.argv[3])

    elif cmd == "interact":
        gen_interact()

    elif cmd == "warmup":
        if len(sys.argv) < 3:
            print("用法: live_gen.py warmup \"产品名\"")
            sys.exit(1)
        gen_warmup(sys.argv[2])

    else:
        print("未知命令: {}".format(cmd))
        sys.exit(1)


if __name__ == "__main__":
    main()
