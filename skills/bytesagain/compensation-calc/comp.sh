## 📈 股权激励方案

### 常见股权激励类型
| 类型 | 说明 | 适用阶段 | 优势 | 劣势 |
|------|------|----------|------|------|
| 期权(ESOP) | 未来以约定价格购买股票的权利 | 创业期 | 激励性强 | 有行权成本 |
| RSU | 满足条件后直接获得股票 | 成熟期 | 无需行权 | 税负较高 |
| 虚拟股权 | 享受分红权但无所有权 | 各阶段 | 灵活简单 | 激励弱于实股 |
| 限制性股票 | 直接授予但有限售期 | 上市公司 | 归属感强 | 前期成本高 |

### 期权方案设计
```
一、期权池
  总量: 公司总股本的 10-20%
  预留: 首次分配 60-70%，预留 30-40%

二、授予对象和数量
  | 层级 | 占期权池比例 | 股数示例 |
  |------|-------------|----------|
  | CTO/VP | 1-3% | |
  | 总监 | 0.5-1% | |
  | 高级工程师 | 0.1-0.5% | |
  | 工程师 | 0.01-0.1% | |

三、成熟条件 (Vesting)
  标准四年期: 4年成熟，1年Cliff
  - 第1年末: 25% 生效
  - 之后每月: 1/48 生效

四、行权价
  - 上一轮融资估值的折扣价
  - 或公司净资产/股数

五、退出机制
  | 情况 | 已成熟部分 | 未成熟部分 |
  |------|-----------|-----------|
  | 主动离职 | 保留，限期行权 | 收回 |
  | 被辞退 | 保留，限期行权 | 收回 |
  | 过失离职 | 收回(可约定) | 收回 |
  | 公司被收购 | 加速成熟 | 按协议 |
```

### 估值参考
```
期权价值 = (公司估值/总股数 - 行权价) × 持有股数

示例：
  公司估值: 1亿
  总股数: 1000万股
  每股价值: 10元
  行权价: 2元
  持有期权: 10000股
  期权价值: (10-2) × 10000 = 8万元
```
