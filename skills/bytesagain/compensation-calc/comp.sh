### 谈判话术

**开场：被问到期望薪资时**
> "基于我了解的市场水平和自身经验，我期望的范围是XX-XX万。
> 当然，我更看重整体的薪酬包和发展机会，您这边的薪资结构是怎样的？"

**应对压价：对方说预算有限**
> "我理解预算的考虑。那我们是否可以探讨一下其他方面的补偿？
> 比如年终奖系数、股权激励、签约奖金、或者更快的调薪周期？"

**亮出竞争offer**
> "很坦诚地说，我目前还有另一个offer，薪资在XX万左右。
> 但我对贵司的业务方向更感兴趣，希望薪资方面能有一定的匹配度。"

