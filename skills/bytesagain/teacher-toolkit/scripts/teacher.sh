#!/usr/bin/env bash
# teacher.sh — 教师工具箱（真实生成版）
# Usage: bash teacher.sh <command> [args...]
# Commands: lesson, exam, rubric, syllabus, report, activity
set -euo pipefail

CMD="${1:-help}"
shift 2>/dev/null || true
INPUT="$*"

# ── 教案生成 ──
generate_lesson() {
  local subject="${1:-数学}"
  local topic="${2:-二次方程}"
  local grade="${3:-高中}"
  local duration="${4:-45}"

  # 计算时间分配
  local intro=$((duration * 10 / 100))
  local teach=$((duration * 35 / 100))
  local practice=$((duration * 30 / 100))
  local discuss=$((duration * 15 / 100))
  local summary=$((duration * 10 / 100))

  cat <<EOF
# 📚 教案设计

> 学科: ${subject} | 课题: ${topic}
> 年级: ${grade} | 课时: ${duration}分钟
> 生成时间: $(date '+%Y-%m-%d %H:%M')

## 一、教学目标

### 知识与技能
1. 理解${topic}的基本概念和核心原理
2. 掌握${topic}的基本方法和解题技巧
3. 能够运用${topic}解决实际问题

### 过程与方法
1. 通过自主探究，培养分析问题的能力
2. 通过小组讨论，发展合作学习能力
3. 通过实践练习，提升知识迁移能力

### 情感态度与价值观
1. 体验${subject}学习的乐趣
2. 培养严谨的学习态度
3. 感受${topic}在生活中的应用价值

## 二、教学重难点

| 类型 | 内容 | 突破策略 |
|------|------|---------|
| **重点** | ${topic}的核心概念理解 | 直观演示+多例对比 |
| **重点** | ${topic}的基本运用 | 分层练习+即时反馈 |
| **难点** | ${topic}的综合应用 | 搭建思维脚手架 |
| **难点** | 从具体到抽象的迁移 | 变式训练+归纳总结 |

## 三、教学过程

### 环节一：导入新课（${intro}分钟）

**🎯 目标：** 激发兴趣，引出课题

**教师活动：**
- 创设情境：展示与${topic}相关的生活场景/问题
- 提出悬念："你们觉得这个问题该怎么解决？"
- 揭示课题：今天我们来学习${topic}

**学生活动：**
- 观察情境，思考问题
- 自由发言，分享想法

**设计意图：** 从生活实例入手，建立学习动机

---

### 环节二：新知教学（${teach}分钟）

**🎯 目标：** 理解概念，掌握方法

**教师活动：**

**【Part 1: 概念建构】** ($(( teach / 3 ))min)
- 引导观察→发现规律→归纳概念
- 板书关键定义和公式
- 💡 教学提示：用"你发现了什么？"代替直接告知

**【Part 2: 方法演示】** ($(( teach / 3 ))min)
- 例题1（基础）：详细讲解完整过程
- 例题2（中等）：师生共同完成
- 重点标注易错点⚠️

**【Part 3: 归纳总结】** ($(( teach / 3 ))min)
- 引导学生总结解题步骤
- 形成方法口诀/流程图
- 对比辨析易混淆概念

**学生活动：**
- 观察、思考、记录
- 跟随演示同步练习
- 尝试用自己的语言复述方法

---

### 环节三：巩固练习（${practice}分钟）

**🎯 目标：** 运用方法，形成技能

| 层次 | 题目类型 | 数量 | 时间 | 方式 |
|------|---------|------|------|------|
| 基础 | 直接运用型 | 2-3题 | $(( practice / 3 ))min | 独立完成 |
| 提高 | 变式运用型 | 2题 | $(( practice / 3 ))min | 同桌讨论 |
| 拓展 | 综合应用型 | 1题 | $(( practice / 3 ))min | 小组合作 |

**即时反馈：**
- 基础题：请学生上台展示
- 提高题：同桌互批+讨论
- 拓展题：小组汇报

---

### 环节四：交流讨论（${discuss}分钟）

**讨论问题：**
1. ${topic}的核心方法是什么？（总结）
2. 在解题时最容易犯什么错？（反思）
3. ${topic}还能用在哪些地方？（迁移）

---

### 环节五：课堂小结（${summary}分钟）

**教师引导：**
- "今天你学到了什么？"
- 思维导图总结（板书/PPT）

**作业布置：**
| 类型 | 内容 | 要求 |
|------|------|------|
| 必做 | 课本P__例题/习题 | 全部 |
| 选做 | 拓展探究题1道 | 有兴趣的同学 |
| 预习 | 下节课内容P__ | 阅读并标记疑问 |

## 四、板书设计

\`\`\`
┌─────────────────────────────────────────┐
│              ${topic}                    │
├──────────────┬──────────────────────────┤
│  一、概念     │   二、方法              │
│              │                         │
│  [定义]      │   步骤1:                │
│  [公式]      │   步骤2:                │
│              │   步骤3:                │
├──────────────┤                         │
│  三、例题     │   四、总结              │
│              │                         │
│  例1:        │   关键点:               │
│  例2:        │   易错点:               │
└──────────────┴──────────────────────────┘
\`\`\`

## 五、教学反思（课后填写）

- 教学目标达成度：___/5
- 学生参与度：___/5
- 时间把控：___/5
- 需改进：_______
- 亮点：_______
EOF
}

# ── 试卷生成 ──
generate_exam() {
  local subject="${1:-数学}"
  local grade="${2:-高中}"
  local duration="${3:-90}"
  local total_score="${4:-100}"

  # 计算各题型分值
  local choice_score=$((total_score * 30 / 100))
  local fill_score=$((total_score * 20 / 100))
  local short_score=$((total_score * 20 / 100))
  local calc_score=$((total_score * 30 / 100))

  local choice_each=3
  local choice_count=$((choice_score / choice_each))
  local fill_each=4
  local fill_count=$((fill_score / fill_each))

  cat <<EOF
# 📝 试卷

**${grade}${subject}测试卷**

| 信息 | 内容 |
|------|------|
| 考试时间 | ${duration}分钟 |
| 满分 | ${total_score}分 |
| 注意事项 | 请用黑色签字笔作答 |

---

姓名：__________ 班级：__________ 学号：__________ 得分：__________

---

## 一、选择题（每小题${choice_each}分，共${choice_score}分）

EOF

  for i in $(seq 1 "$choice_count"); do
    echo "${i}. [题目内容]"
    echo ""
    echo "   A. [选项A]    B. [选项B]    C. [选项C]    D. [选项D]"
    echo ""
  done

  cat <<EOF
## 二、填空题（每空${fill_each}分，共${fill_score}分）

EOF

  for i in $(seq 1 "$fill_count"); do
    local num=$((choice_count + i))
    echo "${num}. [题目内容] ________"
    echo ""
  done

  cat <<EOF
## 三、简答题（共${short_score}分）

$((choice_count + fill_count + 1)). (${short_score}/2分) [题目内容]

$((choice_count + fill_count + 2)). (${short_score}/2分) [题目内容]

## 四、计算/论述题（共${calc_score}分）

$((choice_count + fill_count + 3)). (${calc_score}/2分) [题目内容]

$((choice_count + fill_count + 4)). (${calc_score}/2分) [题目内容]

---

## 📋 答案与评分标准

### 一、选择题答案
| 题号 | $(seq -s ' | ' 1 $choice_count) |
|------|$(printf '---|%.0s' $(seq 1 $choice_count))|
| 答案 | $(for i in $(seq 1 $choice_count); do printf "%s " "$(echo "A B C D" | tr ' ' '\n' | shuf -n1)"; done)|

### 评分说明
- 选择题: 选对得${choice_each}分，错选不得分
- 填空题: 每空${fill_each}分，部分正确酌情给分
- 简答题: 按要点给分，要点完整+逻辑清晰=满分
- 计算题: 列式正确得30%分，过程正确得40%分，结果正确得30%分

### 分数分布
| 题型 | 分值 | 题量 | 难度分布 |
|------|------|------|---------|
| 选择题 | ${choice_score}分 | ${choice_count}题 | 基础60%+中等30%+难10% |
| 填空题 | ${fill_score}分 | ${fill_count}题 | 基础50%+中等40%+难10% |
| 简答题 | ${short_score}分 | 2题 | 中等60%+难40% |
| 计算题 | ${calc_score}分 | 2题 | 中等40%+难60% |

### 难度系数目标: 0.65-0.75
EOF
}

# ── 评分量表(Rubric) ──
generate_rubric() {
  local task="${1:-课堂展示}"
  local max_score="${2:-100}"

  local excellent=$((max_score * 90 / 100))
  local good=$((max_score * 75 / 100))
  local passing=$((max_score * 60 / 100))

  cat <<EOF
# 📊 评分量表 (Rubric) — ${task}

> 满分: ${max_score}分
> 生成时间: $(date '+%Y-%m-%d %H:%M')

## 评分标准

| 维度 | 权重 | 优秀(90-100%) | 良好(75-89%) | 及格(60-74%) | 不及格(<60%) |
|------|------|-------------|-------------|-------------|-------------|
| **内容质量** | 30% | 内容准确完整，有深度见解 | 内容正确，覆盖要点 | 内容基本正确，有遗漏 | 内容错误或严重不完整 |
| **逻辑结构** | 20% | 结构清晰，逻辑严密 | 结构合理，逻辑通顺 | 结构基本完整 | 结构混乱，逻辑不通 |
| **表达能力** | 20% | 表达流畅，用语专业 | 表达清楚，基本准确 | 能理解但有歧义 | 表达不清，难以理解 |
| **创新思维** | 15% | 有独到见解和创意 | 有一定个人思考 | 基本复述已有观点 | 无个人思考 |
| **规范性** | 15% | 格式规范，引用正确 | 格式基本规范 | 格式有欠缺 | 格式混乱 |

## 等级划分

| 等级 | 分数范围 | 描述 |
|------|---------|------|
| A (优秀) | ${excellent}-${max_score} | 全面超出预期 |
| B (良好) | ${good}-$((excellent-1)) | 达到预期 |
| C (及格) | ${passing}-$((good-1)) | 基本达到要求 |
| D (不及格) | 0-$((passing-1)) | 未达到基本要求 |

## 评分表

| 学号 | 姓名 | 内容(30) | 逻辑(20) | 表达(20) | 创新(15) | 规范(15) | 总分 | 等级 |
|------|------|---------|---------|---------|---------|---------|------|------|
| 01 | | | | | | | | |
| 02 | | | | | | | | |
| 03 | | | | | | | | |
| 04 | | | | | | | | |
| 05 | | | | | | | | |

## 评价反馈模板

### 优秀评语
> ${task}表现出色！内容深入全面，逻辑清晰，展现了优秀的[分析/表达/创新]能力。特别值得肯定的是[具体亮点]。建议继续保持，并可以尝试[更高挑战]。

### 良好评语
> ${task}完成良好，基本达到了学习目标。[具体优点]做得不错。建议在[具体改进方向]方面进一步加强，相信下次会更好。

### 需改进评语
> ${task}有一定的努力，但在[具体不足]方面还需要加强。建议：1.[具体建议1] 2.[具体建议2]。有疑问可以来找老师讨论。
EOF
}

# ── 课程大纲 ──
generate_syllabus() {
  local course="${1:-课程名称}"
  local weeks="${2:-16}"
  local hours="${3:-3}"

  local total_hours=$((weeks * hours))

  cat <<EOF
# 📖 课程大纲 — ${course}

> 学时: ${total_hours}学时 (${weeks}周 × ${hours}课时/周)
> 生成时间: $(date '+%Y-%m-%d %H:%M')

## 一、课程基本信息

| 项目 | 内容 |
|------|------|
| 课程名称 | ${course} |
| 课程编号 | [编号] |
| 学分 | $(echo "scale=1; $total_hours / 16" | bc) |
| 总学时 | ${total_hours} (理论: $((total_hours * 70 / 100)) + 实践: $((total_hours * 30 / 100))) |
| 授课对象 | [年级/专业] |
| 先修课程 | [课程列表] |
| 教材 | [主教材名称] |
| 参考书 | [参考教材列表] |

## 二、课程目标

1. **知识目标**: 系统掌握${course}的基本理论和核心概念
2. **能力目标**: 能够运用所学知识分析和解决实际问题
3. **素养目标**: 培养严谨的学术态度和创新思维

## 三、教学进度

| 周次 | 主题 | 学时 | 教学内容 | 作业/活动 |
|------|------|------|---------|----------|
EOF

  local topics=("课程导论与概述" "基础概念与原理" "核心理论(一)" "核心理论(二)" "方法与技术(一)" "方法与技术(二)" "案例分析(一)" "期中复习与测试" "进阶专题(一)" "进阶专题(二)" "实践项目(一)" "实践项目(二)" "前沿发展与趋势" "综合应用" "课程回顾与答疑" "期末考试")
