  for w in $(seq 1 "$weeks"); do
    local idx=$(( (w - 1) % ${#topics[@]} ))
    echo "| 第${w}周 | ${topics[$idx]} | ${hours} | [详细内容] | ${activities[$idx]} |"
  done

  cat <<EOF

## 四、考核方式

| 考核项目 | 比例 | 说明 |
|---------|------|------|
| 平时成绩 | 20% | 出勤(5%) + 课堂参与(15%) |
| 作业 | 20% | ${weeks}次作业，取最高$((weeks * 80 / 100))次平均分 |
| 期中测试 | 20% | 第8周闭卷考试 |
| 期末考试 | 40% | 闭卷考试 |

### 成绩等级

| 分数 | 等级 | GPA |
|------|------|-----|
| 90-100 | A | 4.0 |
| 85-89 | A- | 3.7 |
| 80-84 | B+ | 3.3 |
| 75-79 | B | 3.0 |
| 70-74 | B- | 2.7 |
| 65-69 | C+ | 2.3 |
| 60-64 | C | 2.0 |
| <60 | F | 0 |

## 五、课堂规则

1. 🕐 准时上课，迟到15分钟记为旷课
2. 📱 上课期间手机静音或关机
3. 📝 作业截止日期不可延期（特殊情况提前申请）
4. 🤝 鼓励讨论，但作业/考试独立完成
5. 📖 学术诚信：抄袭/作弊按学校规定处理
EOF
}

# ── 课堂活动设计 ──
generate_activity() {
  local type="${1:-讨论}"
  local duration="${2:-15}"

  cat <<EOF
# 🎲 课堂互动活动 — ${type}

> 时长: ${duration}分钟
> 生成时间: $(date '+%Y-%m-%d %H:%M')

## 活动库（10种课堂活动）

### 1. 🤔 思考-配对-分享 (Think-Pair-Share) — 5分钟
1. 学生独立思考问题 (1min)
2. 与同桌讨论 (2min)
3. 分享给全班 (2min)
**适用：** 概念理解、观点辨析

### 2. 🧩 拼图法 (Jigsaw) — 15分钟
1. 每组分配不同的材料/话题
2. 组内成为"专家"
3. 重新组队，每人讲述自己的部分
**适用：** 多角度理解复杂话题

### 3. 🏆 小组竞赛 — 10分钟
1. 分组（4-5人/组）
2. 抢答/限时答题
3. 积分制，给予奖励
**适用：** 复习巩固、知识检验

### 4. 📋 一分钟论文 (One-Minute Paper) — 3分钟
1. 课末提问："今天最重要的概念是什么？"
2. 学生在纸上写1分钟
3. 收集分析→下节课反馈
**适用：** 即时反馈、查漏补缺

### 5. 🎭 角色扮演 — 15分钟
1. 分配角色（正方/反方/观察者）
2. 根据角色立场讨论
3. 换位思考总结
**适用：** 培养多角度思维

### 6. 🗺️ 概念图 (Concept Map) — 10分钟
1. 给出核心概念
2. 学生画出关联关系
3. 展示对比不同版本
**适用：** 知识体系建构

### 7. ❓ 苏格拉底提问法 — 10分钟
1. 不直接给答案
2. 层层递进提问引导
3. 学生自己得出结论
**适用：** 深度理解、批判性思维

### 8. 📊 四角站队 — 5分钟
1. 教室四角贴A/B/C/D
2. 给出有争议的观点
3. 学生站到同意的角落
4. 各组阐述理由
**适用：** 活跃气氛、观点碰撞

### 9. 🔄 同伴教学 (Peer Instruction) — 10分钟
1. 出一道概念题→学生投票
2. 与持不同答案的同学讨论
3. 再次投票→讲解
**适用：** 纠正常见误解

### 10. 📝 出题者 — 10分钟
1. 学生自己出1道题
2. 交给其他同学做
3. 互相评分+讨论
**适用：** 深度理解、元认知

---

## ⏱️ 时间管理提示

| 活动时长 | 推荐活动 |
|---------|---------|
| 3-5分钟 | 一分钟论文、四角站队 |
| 5-10分钟 | Think-Pair-Share、概念图、同伴教学 |
| 10-15分钟 | 小组竞赛、角色扮演、拼图法 |
| 15-20分钟 | 项目汇报、辩论赛 |
EOF
}

