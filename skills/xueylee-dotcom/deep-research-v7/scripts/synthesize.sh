echo "✅ 已生成3个主题简报:"
ls -la "$BRIEFS_DIR/theme-"*.md

# 5. 生成最终报告
echo ""
echo "=== 步骤4: 生成最终报告 ==="

cat > "$REPORTS_DIR/final-report.md" << 'ENDREPORT'
