class ChineseDictionary {
  constructor() {
    this.wordSet = new Set();
    this.maxWordLength = 1;
    this.wordFrequency = new Map();
    this.loadWords();
  }

  loadWords() {
    let frequency = 1;
    const categories = Object.keys(CHINESE_DICTIONARY);
    const totalCategories = categories.length;
    
    categories.forEach((category, catIndex) => {
      const words = CHINESE_DICTIONARY[category];
      const baseFreq = (totalCategories - catIndex) * 1000;
      
      words.forEach((word, wordIndex) => {
        if (word && word.length > 0) {
          this.wordSet.add(word);
          
          if (word.length > this.maxWordLength) {
            this.maxWordLength = word.length;
          }
          
          const wordFreq = baseFreq + (words.length - wordIndex);
          this.wordFrequency.set(word, wordFreq);
        }
      });
    });
    
    this.maxWordLength = Math.min(this.maxWordLength, 8);
    
    console.log(`词典加载完成: ${this.wordSet.size} 个词, 最大词长 ${this.maxWordLength}`);
  }

  has(word) {
    return this.wordSet.has(word);
  }

  getFrequency(word) {
    return this.wordFrequency.get(word) || 0;
  }

  getMaxWordLength() {
    return this.maxWordLength;
  }

  isStopword(word) {
    return STOPWORDS.has(word);
  }

  getAllWords() {
    return Array.from(this.wordSet);
  }

  addWord(word, frequency = 1000) {
    if (word && word.length > 0) {
      this.wordSet.add(word);
      this.wordFrequency.set(word, frequency);
      if (word.length > this.maxWordLength) {
        this.maxWordLength = Math.min(word.length, 8);
      }
    }
  }

  addWords(words, frequency = 1000) {
    words.forEach(word => this.addWord(word, frequency));
  }

  getStats() {
    const categoryStats = {};
    Object.keys(CHINESE_DICTIONARY).forEach(category => {
      categoryStats[category] = CHINESE_DICTIONARY[category].length;
    });
    
    return {
      totalWords: this.wordSet.size,
      maxWordLength: this.maxWordLength,
      categories: categoryStats
    };
  }
}

