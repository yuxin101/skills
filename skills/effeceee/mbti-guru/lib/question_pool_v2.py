def generate_question_pool():
    """生成完整题库"""
    all_questions = []
    
    # EI
    for pref, opt_a, opt_b in EI_BASE:
        all_questions.append({
            "dimension": "EI",
            "preference": pref,
            "option_a": opt_a,
            "option_b": opt_b
        })
    
    # SN
    for pref, opt_a, opt_b in SN_BASE:
        all_questions.append({
            "dimension": "SN",
            "preference": pref,
            "option_a": opt_a,
            "option_b": opt_b
        })
    
    # TF
    for pref, opt_a, opt_b in TF_BASE:
        all_questions.append({
            "dimension": "TF",
            "preference": pref,
            "option_a": opt_a,
            "option_b": opt_b
        })
    
    # JP
    for pref, opt_a, opt_b in JP_BASE:
        all_questions.append({
            "dimension": "JP",
            "preference": pref,
            "option_a": opt_a,
            "option_b": opt_b
        })
    
    return all_questions


def get_question_pool() -> Dict[str, List[Dict]]:
    """按维度分组获取题库"""
    all_qs = generate_question_pool()
    
    pool = {"EI": [], "SN": [], "TF": [], "JP": []}
    for q in all_qs:
        pool[q["dimension"]].append(q)
    
    return pool


def sample_questions(version: int, seed: int = None) -> List[Dict]:
    """
    从题库中随机抽取题目
    确保每次测试题目不同
    """
    if seed is not None:
        random.seed(seed)
    
    pool = get_question_pool()
    
    # 每个维度需要抽取的题目数
    dim_counts = {
        70: {"EI": 18, "SN": 18, "TF": 18, "JP": 16},
        93: {"EI": 23, "SN": 24, "TF": 24, "JP": 22},
        144: {"EI": 36, "SN": 36, "TF": 36, "JP": 36},
        200: {"EI": 50, "SN": 50, "TF": 50, "JP": 50},
    }
    
    counts = dim_counts.get(version, dim_counts[70])
    
    selected = []
    for dim, count in counts.items():
        available = pool[dim]
        sampled = random.sample(available, min(count, len(available)))
        selected.extend(sampled)
    
    # 打乱顺序
    random.shuffle(selected)
    
    # 重新编号
    for i, q in enumerate(selected, 1):
        q["id"] = str(i)
    
    return selected


def get_pool_stats() -> Dict:
    """获取题库统计"""
    pool = get_question_pool()
    return {
        "EI": len(pool["EI"]),
        "SN": len(pool["SN"]),
        "TF": len(pool["TF"]),
        "JP": len(pool["JP"]),
        "total": sum(len(v) for v in pool.values()),
    }


