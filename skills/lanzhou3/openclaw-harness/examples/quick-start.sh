#!/bin/bash
# =============================================================================
# quick-start.sh — openclaw-harness 快速开始示例
#
# 描述：演示 Harness 基本流程，初始化 → 检查点 → 验证 → GC
# 依赖：harness 已安装于 /data/openclaw-harness/bin/harness
# =============================================================================

set -e

HARNESS="/data/openclaw-harness/bin/harness"
PROJECT="${PROJECT:-$HOME/tmp/quickstart-demo}"

# -----------------------------------------------------------------------------
# 颜色输出
# -----------------------------------------------------------------------------
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

info()    { echo -e "${BLUE}[INFO]${NC}  $*"; }
success() { echo -e "${GREEN}[PASS]${NC}  $*"; }
warn()    { echo -e "${YELLOW}[WARN]${NC}  $*"; }
error()   { echo -e "${RED}[ERROR]${NC} $*"; exit 1; }

# -----------------------------------------------------------------------------
# 初始化
# -----------------------------------------------------------------------------
echo ""
echo "=============================================="
echo "  openclaw-harness 快速开始"
echo "=============================================="
echo ""

# 创建演示项目目录
info "创建演示项目目录: $PROJECT"
mkdir -p "$PROJECT"
cd "$PROJECT"

# 模拟 TASKS.md
cat > TASKS.md <<'EOF'
# TASKS.md

## 当前任务

- [ACTIVE] openclaw-harness 快速开始演示

## 进度

| 阶段 | 状态 |
|------|------|
| 初始化 | ✅ |
| 检查点 | 🔄 |
| 验证 | ⏳ |
| GC | ⏳ |
EOF

# 检查 harness 是否可用
"$HARNESS" --version 2>/dev/null || warn "harness 未返回版本信息（可能正常）"

# -----------------------------------------------------------------------------
# Step 1: 初始化
# -----------------------------------------------------------------------------
echo ""
info "Step 1: 初始化 Harness"
"$HARNESS" init --quiet && success "Harness 初始化完成" || error "初始化失败"

# 验证 .harness 目录已创建
[[ -d ".harness" ]] || error ".harness 目录未创建"
[[ -f ".harness/.initialized" ]] || error ".initialized 标记未创建"
success ".harness/ 目录结构已创建"

# -----------------------------------------------------------------------------
# Step 2: 创建检查点
# -----------------------------------------------------------------------------
echo ""
info "Step 2: 创建检查点"

"$HARNESS" checkpoint create "init-done" --tag "init" && \
  success "检查点 'init-done' 创建成功" || error "检查点创建失败"

# 列出检查点
info "当前检查点列表："
"$HARNESS" checkpoint list 2>/dev/null | head -20 || true

# -----------------------------------------------------------------------------
# Step 3: 模拟开发
# -----------------------------------------------------------------------------
echo ""
info "Step 3: 模拟开发工作"

# 模拟修改 TASKS.md
cat >> TASKS.md <<'EOF'

## 开发记录

- 2026-03-27: 初始化 Harness
EOF
success "TASKS.md 已更新"

# 再创建一个检查点
sleep 1
"$HARNESS" checkpoint create "dev-in-progress" --tag "dev" && \
  success "检查点 'dev-in-progress' 创建成功"

# -----------------------------------------------------------------------------
# Step 4: 验证
# -----------------------------------------------------------------------------
echo ""
info "Step 4: 运行验证"
"$HARNESS" verify && success "验证通过" || warn "验证有失败项，请检查报告"

# -----------------------------------------------------------------------------
# Step 5: GC 预览
# -----------------------------------------------------------------------------
echo ""
info "Step 5: GC 干运行（预览）"
"$HARNESS" gc --dry-run || warn "GC 预览执行异常"

# -----------------------------------------------------------------------------
# Step 6: 查看状态
# -----------------------------------------------------------------------------
echo ""
info "Step 6: 查看 Harness 状态"
"$HARNESS" status -v

# -----------------------------------------------------------------------------
# 完成
# -----------------------------------------------------------------------------
echo ""
echo "=============================================="
echo "  ✅ 快速开始演示完成！"
echo "=============================================="
echo ""
info "检查点已创建，可使用以下命令恢复："
echo "  cd $PROJECT"
echo "  $HARNESS checkpoint list"
echo "  $HARNESS checkpoint restore <cp-id>"
echo ""
info "清理演示目录："
echo "  rm -rf $PROJECT"
echo ""
