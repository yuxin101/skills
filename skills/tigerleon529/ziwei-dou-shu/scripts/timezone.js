    '吉林': 125.3,
    '长春': 125.3,
    '黑龙江': 126.6,
    '哈尔滨': 126.6,
    '江苏': 118.8,
    '南京': 118.8,
    '浙江': 120.2,
    '杭州': 120.2,
    '安徽': 117.3,
    '合肥': 117.3,
    '福建': 119.3,
    '福州': 119.3,
    '江西': 115.9,
    '南昌': 115.9,
    '山东': 117.0,
    '济南': 117.0,
    '河南': 113.6,
