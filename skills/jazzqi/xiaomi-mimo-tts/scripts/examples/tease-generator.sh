#!/bin/bash
# 俏皮话生成器 - 支持多种方言
# 为朋友创作有趣的俏皮话，支持多种方言风格
#
# 用法: ./tease-generator.sh 朋友名字 [方言]
# 示例: ./tease-generator.sh 赵冬 上海话
#       ./tease-generator.sh 张三 四川话
#       ./tease-generator.sh 李四 山东话

NAME="$1"
DIALECT="${2:-普通话}"

# 检查参数
if [ -z "$NAME" ]; then
    echo "用法: $0 朋友名字 [方言]"
    echo ""
    echo "支持的方言:"
    echo "  普通话   上海话   四川话   山东话   台湾腔"
    echo "  东北话   粤语     河南话   陕西话   台湾闽南话"
    echo ""
    echo "示例:"
    echo "  $0 赵冬 上海话"
    echo "  $0 张三 四川话"
    echo "  $0 李四 山东话"
    exit 1
fi

echo "📝 生成 ${DIALECT} 版本的俏皮话给 ${NAME}："
echo "=========================================="

case "$DIALECT" in
    "上海话")
        TEXT="${NAME}侬哪能意思啦？冬天到了侬也跟着冬眠了是伐？手机是死掉了还是信号坏掉了？再勿出来，我就当侬被冬天收走了！侬覅以为躲起来就寻勿到侬了，上海人有的是办法。快点出来，勿然我就当侬是真的傻了呆了！"
        ;;
    "四川话"|"成都话")
        TEXT="哎呀，${NAME}你这个四川话嘛...还算要得！不过要是想更地道点儿，就跟到我们成都人学嘛！巴适得板，安逸惨！你莫着急，慢慢来，要得！"
        ;;
    "山东话")
        TEXT="${NAME}你听！杠赛来你这个山东话！俺滴个娘嘞，恁这个话讲得！得劲！正宗不正宗俺不敢说，跟俺老家讲滴山东话，一样不一样？一样！杠赛！得劲！熊样！"
        ;;
    "台湾腔")
        TEXT="${NAME}诶～你是安捏啦？金价齁，揪消失喔！手机是坏掉哦？还是被冬天冻到秀逗了？再不出现，我就当你是去冬眠了啦！赶紧出来喔，不然真的以为你傻了呆了齁！"
        ;;
    "台湾闽南话")
        TEXT="${NAME}诶～金价喔！哩厚！甲霸没？你这个话讲得足水！有够赞！多谢喔，拍谢拍谢！"
        ;;
    "东北话")
        TEXT="${NAME}啊${NAME}，你是被冬天冻傻了吗？咋跟个冰雕似的，动不动就玩消失？手机是被冬雪埋了，还是信号被寒冬屏蔽了？再不冒泡，我就当你去冬眠了！赶紧出来，不然真以为你傻了呆了！嗷嗷的！"
        ;;
    "粤语")
        TEXT="${NAME}啊，你做乜嘢啊？唔系嘛？手机系唔系坏咗啊？点解咁嘅？冇问题，唔紧要！快啲出来啦！"
        ;;
    "河南话")
        TEXT="${NAME}啊，中不中你这个话？得劲得很！俺说个地道河南顺口溜：恁说中不中？得劲得很！早上喝碗胡辣汤，晌午吃碗烩面，美嘞很！"
        ;;
    "陕西话")
        TEXT="${NAME}啊，嫽咋咧你这个话！美滴很！咥饭了没？谝一谝嘛！嘹太太！"
        ;;
    *)
        # 默认普通话
        TEXT="${NAME}啊${NAME}，你是被冬天冻傻了吗？咋跟个冰雕似的，动不动就玩消失？手机是被冬雪埋了，还是信号被寒冬屏蔽了？再不冒泡，我就当你去冬眠了！赶紧出来，不然真以为你傻了呆了！"
        ;;
esac

echo "$TEXT"
echo ""
echo "🎤 要生成语音吗？(y/n)"
read -r GENERATE

if [[ "$GENERATE" == "y" || "$GENERATE" == "Y" ]]; then
    OUTPUT_FILE="/tmp/tease-${NAME}-${DIALECT}-$(date +%s).ogg"
    echo "正在生成语音..."
    
    # 使用智能版本生成语音
    if [ -f "../mimo-tts-smart.sh" ]; then
        ../mimo-tts-smart.sh "$TEXT" "$OUTPUT_FILE"
    else
        echo "错误: 找不到 mimo-tts-smart.sh 脚本"
        exit 1
    fi
    
    if [ $? -eq 0 ]; then
        echo "✅ 语音生成完成: $OUTPUT_FILE"
    else
        echo "❌ 语音生成失败"
    fi
fi