def build_faq(industry: str) -> str:
    """生成FAQ文档"""
    faqs = FAQ_DATABASE.get(industry, [])
    if not faqs:
        return f"暂无「{industry}」行业的FAQ数据。\n\n可用行业: {', '.join(FAQ_DATABASE.keys())}"

    report = f"# {industry}行业常见法律问题FAQ\n\n"
    report += f"**生成时间**: {datetime.now().strftime('%Y-%m-%d %H:%M')}\n"
    report += f"**问题数量**: {len(faqs)}\n\n"
    report += "---\n\n"

    for i, faq in enumerate(faqs, 1):
        report += f"## Q{i}: {faq['q']}\n\n"
        report += f"**答**: {faq['a']}\n\n"
        report += "---\n\n"

    report += "⚠️ *以上问答仅供参考，具体法律事务请咨询专业律师。*\n"
    return report


def main():
    import argparse
    parser = argparse.ArgumentParser(description="FAQ构建器")
    parser.add_argument("--industry", required=True, help="行业类型")
    parser.add_argument("--output", "-o")
    args = parser.parse_args()

    result = build_faq(args.industry)
    if args.output:
        with open(args.output, "w", encoding="utf-8") as f:
            f.write(result)
        print(f"✅ FAQ已保存至: {args.output}")
    else:
        print(result)


if __name__ == "__main__":
    main()
