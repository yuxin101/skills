#!/usr/bin/env python3
"""
技能ID: workplace-harassment-policy
技能名称: 职场反骚扰制度生成器
模块: 劳动合规
功能: 生成合规的职场反骚扰制度文档
"""

import json
from typing import Dict, Any

def skill_function(input_data: Dict[str, Any]) -> Dict[str, Any]:
    try:
        company_name = input_data.get("company_name", "XX公司")
        industry = input_data.get("industry", "通用")
        contact_person = input_data.get("contact_person", "人力资源部")

        policy = f"""# {company_name} 反职场骚扰管理制度

## 第一章 总则

**第一条** 为保障员工合法权益，预防和制止职场骚扰行为，根据《民法典》第1010条、《妇女权益保障法》及相关法律法规，结合公司实际，制定本制度。

**第二条** 本制度适用于公司全体员工，包括正式员工、实习生、劳务派遣人员及临时用工。

**第三条** 公司坚持零容忍原则，严禁任何形式的职场骚扰行为。

## 第二章 骚扰行为定义

**第四条** 职场骚扰包括但不限于以下类型：

### （一）性骚扰
- 不受欢迎的性暗示言语、文字、图像
- 不适当的身体接触或故意靠近
- 以性为条件的利益交换（如升职、加薪）
- 发送不雅信息、图片或视频

### （二）职场霸凌
- 持续性的言语侮辱、人身攻击
- 故意孤立、排挤特定员工
- 不合理的工作安排（如故意刁难、超负荷分配）
- 恶意传播谣言或隐私

### （三）歧视性骚扰
- 基于性别、年龄、民族、宗教、残疾等的歧视性言论
- 贬低特定群体的笑话或言论

## 第三章 投诉渠道

**第五条** 员工遭遇或发现骚扰行为，可通过以下渠道投诉：

1. **直接投诉**: 向直属上级或{contact_person}反映
2. **匿名投诉**: 通过公司邮箱 hr@company.com（匿名可选）
3. **紧急投诉**: 拨打公司反骚扰热线 XXX-XXXX-XXXX
4. **外部投诉**: 向劳动监察部门或公安机关报案

**第六条** 公司承诺对投诉人信息严格保密，禁止任何报复行为。

## 第四章 调查处理程序

**第七条** 收到投诉后，公司应在**3个工作日内**启动调查。

**第八条** 调查程序：
1. 成立调查组（至少3人，含HR、法务、员工代表）
2. 分别约谈投诉人、被投诉人及相关证人
3. 收集书面证据、聊天记录、监控录像等
4. **15个工作日内**完成调查并出具调查报告

**第九条** 根据调查结果采取相应措施：
- 情节轻微：批评教育、书面警告
- 情节较重：降职降薪、调离岗位
- 情节严重：解除劳动合同
- 涉嫌违法犯罪：移送公安机关

## 第五章 预防措施

**第十条** 公司应定期开展反骚扰培训，每年不少于1次。

**第十一条** 新员工入职培训须包含反骚扰制度内容。

**第十二条** 公司应建立安全的工作环境，如安装监控、设置独立办公区域等。

## 第六章 附则

**第十三条** 本制度自发布之日起施行，由人力资源部负责解释。

**第十四条** 本制度如与国家法律法规相冲突，以法律法规为准。

---
*生效日期: {__import__('datetime').date.today().isoformat()}*
*适用行业: {industry}*
"""

        return {
            "status": "success",
            "result": {
                "制度文档": policy,
                "字数": len(policy),
                "覆盖要点": ["性骚扰定义", "职场霸凌定义", "投诉渠道", "调查程序", "处理措施", "预防措施"],
            },
            "法律依据": "《民法典》第1010条、《妇女权益保障法》第23条、《劳动法》",
        }
    except Exception as e:
        return {"status": "error", "message": str(e)}

if __name__ == "__main__":
    print(json.dumps(skill_function({"company_name": "示例科技有限公司", "industry": "互联网"}), ensure_ascii=False, indent=2))
